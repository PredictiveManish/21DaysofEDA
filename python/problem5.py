# Suggest an activity based on the weather (e.g Sunny- Go for a walk, Rainy- Read a book, Snowy - Build a snowman.)

weather = str(input("How is weather in your surroundings?"))

if weather == 'Sunny':
    print("Go for a walk.")
elif weather == 'Rainy':
    print("Read a book.")
elif weather=='Snowy':
    print("Build a snowman")
else:
    print("Go and study man, it will give you good life.")

